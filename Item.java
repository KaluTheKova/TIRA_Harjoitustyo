public class Item {
    /* A class represnting a single item in a hashtable */
    
    // Attributes
    private String value;

    // Constructor
    public Item(String value) {
        this.value = value;
    }
    
    // Getters
    public String getValue() {
        return value;
    }

    // Setters
    public void setValue(String value) {
        this.value = value;
    }
}